numero_um = int (input("Digite o primeiro numero:"))
numero_dois = int (input("Digite o segundo numero:"))
soma = numero_um + numero_dois
sub = numero_um - numero_dois
mult = numero_um * numero_dois
div = numero_um / numero_dois


print ("a soma dos dois numeros resulta em:", soma)
print ("a subtração dos dois numeros resulta em:", sub)
print ("a multiplicação dos dois numeros resulta em:", mult)
print ("a divisão dos dois numeros resulta em:", div)